package org.pattern.facade.finalsol;

public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn Popper is ON");
    }

    public void off() {
        System.out.println("Popcorn Popper is OFF");
    }

    public void pop() {
        System.out.println("Popcorn is popped");
    }
}
